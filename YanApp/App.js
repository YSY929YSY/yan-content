function StrokeMiniPath({ d, delay, dashLen, tx = 0, ty = 0, scale = 1, matrixTransform }) {
  const progress = useRef(new Animated.Value(0)).current;

  useEffect(() => {
    progress.setValue(0);
    Animated.timing(progress, {
      toValue: 1,
      duration: 780,
      delay,
      useNativeDriver: false,
    }).start();
  }, [d, delay, dashLen]);

  const dashOffset = progress.interpolate({
    inputRange: [0, 1],
    outputRange: [dashLen, 0],
  });

  const transform = [];
  if (matrixTransform) {
    transform.push(matrixTransform);
  } else {
    if (scale !== 1) transform.push(`scale(${scale})`);
    if (tx || ty) transform.push(`translate(${tx} ${ty})`);
  }

  return (
    <AnimatedPath
      d={d}
      stroke="#3A2721"
      strokeWidth="4.5"
      strokeLinecap="round"
      strokeLinejoin="round"
      fill="none"
      opacity={0.98}
      strokeDasharray={dashLen}
      strokeDashoffset={dashOffset}
      transform={transform.length ? transform.join(' ') : undefined}
    />
  );
}
