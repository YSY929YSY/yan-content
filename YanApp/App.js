function resolveMiniStrokes(strokeKey, seen = new Set()) {
  if (seen.has(strokeKey)) return [];
  const combo = STROKE_COMBOS[strokeKey];
  if (!combo) return MINI_STROKES[strokeKey] || [];
  const nextSeen = new Set(seen);
  nextSeen.add(strokeKey);
  const markOffset = COMBO_MARK_OFFSETS[strokeKey];
  return combo.flatMap(component => {
    const comboItem = typeof component === 'string' ? { key: component } : component;
    const strokes = resolveMiniStrokes(comboItem.key, nextSeen);
    return strokes.map(stroke => {
      const result = { ...stroke };
      if (comboItem.scale && comboItem.scale !== 1) {
        const scale = (result.scale || 1) * comboItem.scale;
        const tx = (result.tx || 0) + (comboItem.tx || 0);
        const ty = (result.ty || 0) + (comboItem.ty || 0);
        result.matrixTransform = `matrix(${scale} 0 0 ${scale} ${tx} ${ty})`;
        return result;
      }
      if (comboItem.tx) result.tx = (result.tx || 0) + comboItem.tx;
      if (comboItem.ty) result.ty = (result.ty || 0) + comboItem.ty;
      if (!comboItem.tx && !comboItem.ty && !comboItem.scale && markOffset && comboItem.key.includes('dakuten')) {
        result.tx = (result.tx || 0) + markOffset.x;
        result.ty = (result.ty || 0) + markOffset.y;
      }
      return result;
    });
  });
}

